    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().isEmpty() || txtAge.getText().isEmpty() || txtPhone.getText().isEmpty() || cmbGender.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information.");
        } else {
            try {
                PreparedStatement stmt = conn.prepareStatement("insert into customer (cusName, cusAge, cusGender, cusPhone) values(?, ?, ?, ?)");
                stmt.setString(1, txtName.getText());
                stmt.setInt(2, Integer.valueOf(txtAge.getText()));
                stmt.setString(3, cmbGender.getSelectedItem().toString());
                stmt.setString(4, txtPhone.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Successfully Added");
                
                showCustomers();
                
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

