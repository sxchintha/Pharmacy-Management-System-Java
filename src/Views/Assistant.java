    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (selectedCusID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No customer selected.");
        } else if (txtName.getText().isEmpty() || txtAge.getText().isEmpty() || txtPhone.getText().isEmpty() || cmbGender.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information.");
        } else {
            try {
                String UpdateQuery = "update customer set cusName= '" + txtName.getText() + "'" + ",cusAge = " + txtAge.getText() + "" + ",cusPhone = '" + txtPhone.getText() + "', cusGender = '" + cmbGender.getSelectedItem().toString() + "'" + " where cusID = " + selectedCusID;
//                System.out.println(UpdateQuery);
                Statement Add = conn.createStatement();
                Add.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Customer Update Successfully.");
            } catch (SQLException e) {
                e.printStackTrace();
            }
            showCustomers();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

